package tugasAlproPekan2;

public class tugasAlproPekan2 {

	public static void main(String[] args) {
		// Deklarasi variabel data mahasiswa
        int nim = 11531012;           // nim mahasiswa
        float ipk = 3.57f;            // nilai ipk mahasiswa (pakai f untuk float)
        char kelas = 'B';             // kelas mahasiswa
        boolean aktif = true;         // status aktif mahasiswa (true = aktif , false = tidak)

        // Output data ke layar
        System.out.println("=== Data Mahasiswa ===");
        System.out.println("NIM        : " + nim);
        System.out.println("IPK        : " + ipk);
        System.out.println("Kelas      : " + kelas);
        System.out.println("Status Aktif: " + aktif);
	}
	
}
